    private void tblDataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDataMouseClicked
        // TODO add your handling code here:
        int baris = tblData.getSelectedRow();
        if (baris != -1) {
            ID.setText(tblData.getValueAt(baris, 0).toString());
            TXTNAMA.setText(tblData.getValueAt(baris, 1).toString());
          if ("Eksekutif".equals(tblData.getValueAt(baris, 5).toString())) {
                EKSE.setSelected(true);
            } else {
                EKO.setSelected(true);
            }
            SimpleDateFormat date = new SimpleDateFormat("yyyy-MM-dd");
            Date dateFormat= null;
            try{
                dateFormat = date.parse(tblData.getValueAt(baris, 2).toString());
                
            } catch (ParseException ex){
                Logger.getLogger(Frame.class.getName()).log(Level.SEVERE, null, ex);
            }
            TGL.setDate(dateFormat);
            STAWAL.setText(tblData.getValueAt(baris, 3).toString());
            STAKHIR.setText(tblData.getValueAt(baris, 4).toString());
            WKT.setText(tblData.getValueAt(baris, 6).toString());
            TMPT.setText(tblData.getValueAt(baris, 7).toString());
            HRG.setText(tblData.getValueAt(baris, 8).toString());
        }
    }//GEN-LAST:event_tblDataMouseClicked

