       private void selectData() {
           String kolom[] = {"id_user","NamaPemesan","TglBerangkat","StAwal","StTujuan","JnsKereta","WaktuBerangkat","TempatDuduk","Harga"};
        DefaultTableModel dtm = new DefaultTableModel(null, kolom);
        String SQL = "SELECT * FROM tb_pemesan";
        ResultSet rs = KoneksiDB.executeQuery(SQL);
        try{
            while(rs.next()){
                String id_user = rs.getString(1);
                String NamaPemesan = rs.getString(2);
                String JnsKereta = "";
                if("Eksekutif".equals(rs.getString(3))){
                    JnsKereta = "Eksekutif";
                }else{
                    JnsKereta = "Ekonomi";
                }
                String TglBerangkat = rs.getString(4);
                String StAwal = rs.getString(5);
                String StTujuan = rs.getString(6);
                
                Integer WaktuBerangkat = rs.getInt(7);
                String TempatDuduk = rs.getString(8);
                Integer Harga = rs.getInt(9);
                
                String data[] = {id_user, NamaPemesan,TglBerangkat,StAwal,JnsKereta};
                
                dtm.addRow(data);
                
            }
        }catch (SQLException ex){
            Logger.getLogger(Frame.class.getName()).log(Level.SEVERE, null, ex);
            
        }
        tblData.setModel(dtm);
    }

